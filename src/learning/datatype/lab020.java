package learning.datatype;

public class lab020 {
    public static void main(String[] args) {
        // these are Escape Characters
        char a='\n'; //New line keyword
        char b='\t'; // Space keyword
        char c='\b'; // Removed one letter of first word
        char d='\r'; // Removed first word from the line.
        char e='\''; // inset a single quote.
        char f='\"'; // inset a double quote.
        char g='\\'; // insert a backlash.
        char h='\f'; // form feed
        System.out.println("Super"+ a +"Charger");
        System.out.println("Super"+ b +"Charger");
        System.out.println("Super"+ c +"Charger");
        System.out.println("Super"+ d +"Charger");
        System.out.println("Super"+ e +"Charger");
        System.out.println("Super"+ f +"Charger");
        System.out.println("Super"+ g +"Charger");
        System.out.println("Super"+ h +"Charger");
    }
}
