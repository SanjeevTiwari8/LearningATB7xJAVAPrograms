package learning.datatype;

public class Lab005 {
    public static void main(String[] args) {
        // how to write data type with variables.
        // datatype variable_name= variable name (literal);
        //Data type--> two types of data types.
        //1-Primitive data types
        // integral-byte/short/int/long(l)
        byte B=10;
        short S=100;
        int INT=120;
        long NUMBER= 7485102369l;
        System.out.println(B);
        System.out.println("");
        System.out.println(S);
        System.out.println("");
        System.out.println(INT);
        System.out.println("");
        System.out.println(NUMBER);
        System.out.println("");

        // char- char(a,b)
        char CHAR_NEW= 'A';
        System.out.println(CHAR_NEW);

        // floating- float(f)/double
        float F_new= 1405.36f;
        double D_new=1402.2253336;
        System.out.println(F_new);
        System.out.println(D_new);

        // boolean- true or false.
        boolean System_working= true;
        boolean System_not_working= false;
        System.out.println(System_working);
        System.out.println(System_not_working);

        //2-Non- primitive data types.
        //String -collection of number or a bunch of character
        // *All the Non-primitive data - with take 8 Bytes and 64 bites.*
        String New_String="Sanjeev Tiwari";
        System.out.println(New_String);
        //2- Enum.
    }
}
