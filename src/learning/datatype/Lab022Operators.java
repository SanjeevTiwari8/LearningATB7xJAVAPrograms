package learning.datatype;

public class Lab022Operators {
    public static void main(String[] args) {
        //compound Assignment operators
        int a=10;
        // +=, -=, *=, /=, %=
        a+=10;// a+1=10;
        a-=10;// a-1=10;
        a*=10;// a*1=10;
        a/=10;// a/1=10;
        System.out.println(a);

    }
}
